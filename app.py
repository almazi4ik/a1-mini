@st.cache_resource
def load_or_train_model():
    model = TinyTransformer(vocab_size, D_MODEL, NHEAD, NUM_LAYERS, MAX_LEN, DROPOUT)
    
    sequences = []
    for q, a in DIALOGS:
        text = q + " > " + a + " <"
        enc = encode(text)
        if len(enc) > 1:
            sequences.append(enc)
    
    progress_bar = st.progress(0)
    status_text = st.empty()
    
    optimizer = torch.optim.Adam(model.parameters(), lr=0.001)
    model.train()
    epochs = 30
    
    for epoch in range(epochs):
        total_loss = 0
        random.shuffle(sequences)
        for seq in sequences:
            if len(seq) < 2:
                continue
            x = torch.tensor(seq[:-1]).unsqueeze(0)
            y = torch.tensor(seq[1:]).unsqueeze(0)
            optimizer.zero_grad()
            logits = model(x)
            loss = F.cross_entropy(logits.squeeze(0), y.squeeze(0))
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            total_loss += loss.item()
        
        if (epoch + 1) % 5 == 0:
            status_text.text(f"Эпоха {epoch+1}/{epochs}, loss: {total_loss/len(sequences):.4f}")
        progress_bar.progress((epoch + 1) / epochs)
    
    status_text.text("Обучение завершено!")
    progress_bar.empty()
    torch.save(model.state_dict(), MODEL_PATH)
    with open(MODEL_PATH + ".ready", "w") as f:
        f.write("ready")
    
    model.eval()
    return model

# --- Генерация ответа ---
def generate_response(prompt, model, max_words=15, temperature=0.6):
    math_result = calculate(prompt)
    if math_result:
        return math_result
    
    if len(prompt) > 0 and not any(c.isalpha() for c in prompt):
        return "извини я не понимаю такие сообщения"
    
    prompt_clean = fix_typo(prompt)
    input_text = prompt_clean + " >"
    enc = encode(input_text)
    
    if not enc:
        return "не понимаю попробуй ещё раз"
    
    x = torch.tensor(enc).unsqueeze(0)
    used = set()
    result = []
    cur = x
    
    with torch.no_grad():
        for _ in range(max_words):
            logits = model(cur)
            last = logits[0, -1, :] / temperature
            probs = F.softmax(last, dim=-1)
            nid = torch.multinomial(probs, 1).item()
            word = i2w[nid]
            if word == "<":
                break
            if word == ">":
                continue
            if word in used and len(used) > max_words // 2:
                continue
            result.append(word)
            used.add(word)
            cur = torch.cat([cur, torch.tensor([[nid]])], dim=1)
    
    response = " ".join(result).strip()
    return response if response else "интересно расскажи больше"

