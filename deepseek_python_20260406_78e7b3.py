# --- Подготовка данных ---
all_text = " ".join([q + " > " + a + " <" for q, a in DIALOGS])
words = sorted(set(all_text.split()))
vocab_size = len(words)
w2i = {w: i for i, w in enumerate(words)}
i2w = {i: w for w, i in w2i.items()}

def encode(s):
    return [w2i[w] for w in s.lower().strip().split() if w in w2i]

# --- Positional Encoding ---
class PositionalEncoding(nn.Module):
    def __init__(self, d_model, max_len=5000, dropout=0.1):
        super().__init__()
        self.dropout = nn.Dropout(p=dropout)
        pe = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len, dtype=torch.float).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model))
        pe[:, 0::2] = torch.sin(position * div_term)
        pe[:, 1::2] = torch.cos(position * div_term)
        pe = pe.unsqueeze(0)
        self.register_buffer('pe', pe)
    
    def forward(self, x):
        x = x + self.pe[:, :x.size(1), :]
        return self.dropout(x)

# --- TinyTransformer ---
class TinyTransformer(nn.Module):
    def __init__(self, vocab_size, d_model=128, nhead=4, num_layers=4, max_len=100, dropout=0.1):
        super().__init__()
        self.d_model = d_model
        self.embedding = nn.Embedding(vocab_size, d_model)
        self.pos_encoder = PositionalEncoding(d_model, max_len, dropout)
        encoder_layer = nn.TransformerEncoderLayer(
            d_model=d_model, nhead=nhead, dim_feedforward=d_model * 4,
            dropout=dropout, batch_first=True
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=num_layers)
        self.fc_out = nn.Linear(d_model, vocab_size)
    
    def forward(self, x):
        x = self.embedding(x) * math.sqrt(self.d_model)
        x = self.pos_encoder(x)
        x = self.transformer(x)
        x = self.fc_out(x)
        return x

# --- Исправление опечаток ---
TYPO_MAP = {
    "првиет": "привет", "првет": "привет", "спс": "спасибо",
    "пака": "пока", "какдила": "как дела", "хоршо": "хорошо"
}

def fix_typo(text):
    words = text.lower().strip().split()
    fixed = []
    for w in words:
        if w in TYPO_MAP:
            fixed.append(TYPO_MAP[w])
        else:
            m = get_close_matches(w, list(w2i.keys()), n=1, cutoff=0.7)
            fixed.append(m[0] if m else w)
    return " ".join(fixed)

# --- Обучение (25 эпох) ---
@st.cache_resource
def load_or_train_model():
    model = TinyTransformer(vocab_size, D_MODEL, NHEAD, NUM_LAYERS, MAX_LEN, DROPOUT)
    
    sequences = []
    for q, a in DIALOGS:
        text = q + " > " + a + " <"
        enc = encode(text)
        if len(enc) > 1:
            sequences.append(enc)
    
    progress_bar = st.progress(0)
    status_text = st.empty()
    
    optimizer = torch.optim.Adam(model.parameters(), lr=0.001)
    model.train()
    epochs = 25
    
    for epoch in range(epochs):
        total_loss = 0
        random.shuffle(sequences)
        for seq in sequences:
            if len(seq) < 2:
                continue
            x = torch.tensor(seq[:-1]).unsqueeze(0)
            y = torch.tensor(seq[1:]).unsqueeze(0)
            optimizer.zero_grad()
            logits = model(x)
            loss = F.cross_entropy(logits.squeeze(0), y.squeeze(0))
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            total_loss += loss.item()
        
        if (epoch + 1) % 5 == 0:
            status_text.text(f"Эпоха {epoch+1}/{epochs}, loss: {total_loss/len(sequences):.4f}")
        progress_bar.progress((epoch + 1) / epochs)
    
    status_text.text("Обучение завершено!")
    progress_bar.empty()
    torch.save(model.state_dict(), MODEL_PATH)
    with open(MODEL_PATH + ".ready", "w") as f:
        f.write("ready")
    
    model.eval()
    return model

# --- Генерация ответа ---
def generate_response(prompt, model, max_words=15, temperature=0.6):
    if len(prompt) > 0 and not any(c.isalpha() for c in prompt):
        return "извини я не понимаю такие сообщения"
    
    prompt_clean = fix_typo(prompt)
    input_text = prompt_clean + " >"
    enc = encode(input_text)
    
    if not enc:
        return "не понимаю попробуй ещё раз"
    
    x = torch.tensor(enc).unsqueeze(0)
    used = set()
    result = []
    cur = x
    
    with torch.no_grad():
        for _ in range(max_words):
            logits = model(cur)
            last = logits[0, -1, :] / temperature
            probs = F.softmax(last, dim=-1)
            nid = torch.multinomial(probs, 1).item()
            word = i2w[nid]
            if word == "<":
                break
            if word == ">":
                continue
            if word in used and len(used) > max_words // 2:
                continue
            result.append(word)
            used.add(word)
            cur = torch.cat([cur, torch.tensor([[nid]])], dim=1)
    
    return " ".join(result).strip() or "интересно расскажи больше"

